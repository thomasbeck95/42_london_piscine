#include <unistd.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

int	main(int argc, char *argv[])
{
	long	len;

	len = ft_strlen(argv[argc - argc]);
	write(1, &*argv[0], len);
	write(1, "\n", 1);
}
